    // Script para mejorar la interactividad de la guía
    $(document).ready(function() {
        // Smooth scroll para enlaces internos
        $('a[href^="#"]').on('click', function(event) {
            var target = $(this.getAttribute('href'));
            if( target.length ) {
                event.preventDefault();
                $('html, body').stop().animate({
                    scrollTop: target.offset().top - 70
                }, 1000);
            }
        });

        // Resaltar sección activa en scroll
        $(window).scroll(function() {
            var scrollDistance = $(window).scrollTop() + 80;

            $('.guide-section').each(function(i) {
                if ($(this).position().top <= scrollDistance) {
                    $('.nav-guide a.active').removeClass('active');
                    $('.nav-guide a').eq(i).addClass('active');
                }
            });
        }).scroll();
    });
