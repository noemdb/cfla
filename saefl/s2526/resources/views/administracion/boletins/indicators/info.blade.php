        <div class="mx-2">

            <div>IEE = (Cantidad de evaluaciones corregidas y cargadas al sistema correctamente / Total de evaluaciones aplicadas) x 100</div>
            <div class="text-muted">
                Este indicador mide la proporción de evaluaciones que son corregidas y cargadas al sistema correctamente en relación con la cantidad total de evaluaciones programadas.
                Un IEE alto indica que el profesor es eficiente y efectivo en la aplicación de las evaluaciones y en su corrección, lo que puede ser una señal de su capacidad para impartir una enseñanza de calidad.
            </div>

