        <div class="mx-2">
            <div class="">IEE-CN = (Cantidad de evaluaciones aplicadas antes del corte de notas corregidas y cargadas al sistema correctamente / Total de evaluaciones programadas para antes del corte de notas) x 100</div>
            <div class="text-muted">
            	Este indicador es el equivalente al IEE, sin embargo toma en cuenta la fecha del corte de notas, lo que significa que 
                mide la proporción de evaluaciones aplicadas para antes del Corte de Notas, que son corregidas y cargadas al sistema correctamente antes de dicha fecha,
                en relación con la cantidad total de evaluaciones programadas para antes de la fecha del Corte de Notas.
                Un IEE-CN alto indica que el profesor es eficiente y efectivo en el cumplimiento de los lapsos establecidos para realizar estas tareas.
