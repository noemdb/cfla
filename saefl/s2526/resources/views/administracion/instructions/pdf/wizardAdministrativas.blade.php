                <!-- Pregunta 2 -->
                <table border="1" cellpadding="10" cellspacing="0" width="100%" bgcolor="#fff3cd">
                    <tr>
                        <td>
                            <font size="4" color="#856404" face="Arial">
                                <strong>¿Cómo se calcula exactamente el recargo por morosidad?</strong>
                            </font>
                            <br><br>
                            <font size="3" face="Arial">El cálculo sigue esta fórmula:</font>
                            <ol>
                                <li><strong>Meses de mora:</strong> Diferencia en meses entre la fecha de inicio de mora y hoy</li>
                                <li><strong>Límite máximo:</strong> 12 meses (12% de recargo)</li>
                                <li><strong>Fórmula:</strong> <code>Recargo = MIN(meses, 12) × (monto_original × 0.01)</code></li>
                                <li><strong>Total:</strong> <code>Monto_original + Recargo</code></li>
                            </ol>
