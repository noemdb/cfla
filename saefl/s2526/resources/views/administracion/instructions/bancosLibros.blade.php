            <div class="accordion" id="faqAccordion">
                <!-- Pregunta 1 -->
                <div class="card">
                    <div class="card-header" id="faq1">
                        <h5 class="mb-0">
                            <button class="btn btn-link" type="button" data-toggle="collapse" data-target="#answer1">
                                <i class="fas fa-question"></i> ¿Por qué algunos pagos no aparecen en el reporte?
                            </button>
                        </h5>
                    </div>
                    <div id="answer1" class="collapse show" data-parent="#faqAccordion">
                        <div class="card-body">
                            Los pagos deben cumplir <strong>tres condiciones</strong> para aparecer:
                            <ol>
                                <li>Tener número de factura asignado (no nulo)</li>
                                <li>Estar dentro del rango de fechas seleccionado</li>
                                <li>Corresponder al banco y método de pago filtrado</li>
                                <li>No estar marcados como eliminados (soft delete)</li>
                            </ol>
                        </div>
                    </div>
                </div>

                <!-- Pregunta 2 -->
                <div class="card">
                    <div class="card-header" id="faq2">
                        <h5 class="mb-0">
                            <button class="btn btn-link collapsed" type="button" data-toggle="collapse" data-target="#answer2">
                                <i class="fas fa-question"></i> ¿Qué diferencia hay entre fecha de pago y fecha de transacción?
                            </button>
                        </h5>
                    </div>
                    <div id="answer2" class="collapse" data-parent="#faqAccordion">
                        <div class="card-body">
                            <strong>Fecha de Pago:</strong> Cuando el representante realizó el pago<br>
                            <strong>Fecha de Transacción:</strong> Cuando el banco procesó la operación<br>
                            <strong>Fecha de Registro:</strong> Cuando se ingresó en el sistema<br><br>
                            En pagos extemporáneos, se usa la fecha de registro para el filtrado.
                        </div>
                    </div>
                </div>

                <!-- Pregunta 3 -->
                <div class="card">
                    <div class="card-header" id="faq3">
                        <h5 class="mb-0">
                            <button class="btn btn-link collapsed" type="button" data-toggle="collapse" data-target="#answer3">
                                <i class="fas fa-question"></i> ¿Por qué el total general no coincide con la suma de los métodos?
                            </button>
                        </h5>
                    </div>
                    <div id="answer3" class="collapse" data-parent="#faqAccordion">
                        <div class="card-body">
                            Esto puede deberse a:
                            <ul>
                                <li><strong>Agrupación diferente:</strong> Totales generales usan sum() directo, mientras métodos usan groupBy()</li>
                                <li><strong>Pagos sin método asignado:</strong> Algunos ingresos podrían no tener método de pago asociado</li>
                                <li><strong>Redondeo de decimales:</strong> Diferencias mínimas por aproximación</li>
                                <li><strong>Filtros aplicados:</strong> Verificar que todos los filtros sean consistentes</li>
                            </ul>
                        </div>
                    </div>
                </div>

                <!-- Pregunta 4 -->
                <div class="card">
                    <div class="card-header" id="faq4">
                        <h5 class="mb-0">
                            <button class="btn btn-link collapsed" type="button" data-toggle="collapse" data-target="#answer4">
