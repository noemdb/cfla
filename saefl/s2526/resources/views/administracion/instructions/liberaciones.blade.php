    @section('scripts')
        @parent
        <script>
            $(document).ready(function() {
                // Activar elementos de la navegación según scroll
                function updateActiveNav() {
                    const sections = $('section');
                    const navLinks = $('.list-group-item');

                    let currentSection = '';
                    const scrollPos = $(window).scrollTop() + 100;

                    sections.each(function() {
                        const sectionTop = $(this).offset().top;
                        const sectionBottom = sectionTop + $(this).outerHeight();

                        if (scrollPos >= sectionTop && scrollPos < sectionBottom) {
                            currentSection = $(this).attr('id');
                            return false;
                        }
                    });

                    navLinks.removeClass('active');
                    navLinks.filter('[href="#' + currentSection + '"]').addClass('active');

                    // Actualizar progreso
                    updateReadingProgress();
                }

                // Actualizar progreso de lectura
                function updateReadingProgress() {
                    const winHeight = $(window).height();
                    const docHeight = $(document).height();
                    const scrollTop = $(window).scrollTop();
                    const scrollPercent = (scrollTop / (docHeight - winHeight)) * 100;

                    $('#readingProgress').css('width', scrollPercent + '%');
                    $('#progressText').text(Math.round(scrollPercent) + '% completado');
                }

                // Inicializar
                updateActiveNav();
                updateReadingProgress();

                // Event listeners
                $(window).on('scroll', function() {
                    updateActiveNav();
                });

                // Smooth scroll para enlaces de navegación
                $('.list-group-item').on('click', function(e) {
                    e.preventDefault();
                    const target = $(this).attr('href');
                    $('html, body').animate({
                        scrollTop: $(target).offset().top - 80
                    }, 800);
                });
            });
        </script>
    @endsection

