            <h4>Proceso de Consulta</h4>
            <div class="info-box">
                <strong><i class="fas fa-database"></i> Estructura de la Consulta:</strong>
                <ul class="mb-0 mt-2">
                    <li><strong>Tabla Principal:</strong> <code>representants</code></li>
                    <li><strong>Joins:</strong> <code>estudiants, inscripcions, seccions, grados, administrativas,
                            planpagos</code></li>
                    <li><strong>Agrupación:</strong> Por <code>representants.id</code></li>
                    <li><strong>Orden:</strong> Alfabético por nombre de representante</li>
                </ul>
            </div>

            <h4>Cálculos Automáticos</h4>
            <div class="row">
                <div class="col-md-8">
                    <div class="table-responsive">
                        <table class="table table-bordered">
                            <thead class="thead-dark">
                                <tr>
                                    <th>Cálculo</th>
                                    <th>Descripción</th>
                                    <th>Fórmula/Fuente</th>
                                </tr>
                            </thead>
                            <tbody>
                                <tr>
                                    <td><strong>Deuda Total Institucional</strong></td>
                                    <td>Suma de todas las deudas vencidas</td>
                                    <td><code>SUM(exchange_ammount_expire_bill)</code></td>
                                </tr>
                                <tr>
                                    <td><strong>Porcentaje de Morosidad</strong></td>
                                    <td>% de representantes con deuda vs total</td>
                                    <td><code>(count(deuda_ex_arr) / total_representants) * 100</code></td>
                                </tr>
                                <tr>
                                    <td><strong>Deuda por Representante</strong></td>
                                    <td>Deuda individual en divisas</td>
                                    <td><code>representant->exchange_ammount_expire_bill</code></td>
                                </tr>
                                <tr>
                                    <td><strong>Saldo a Favor</strong></td>
                                    <td>Créditos disponibles del representante</td>
                                    <td><code>total_credito_exchange + total_abono_exchange</code></td>
                                </tr>
                                <tr>
                                    <td><strong>Deuda Neta</strong></td>
                                    <td>Deuda menos saldo a favor</td>
                                    <td><code>exchange_ammount_expire_bill - saldo_a_favor_exchange</code></td>
                                </tr>
                            </tbody>
                        </table>
                    </div>
                </div>
                <div class="col-md-4">
                    <div class="tip-box">
                        <strong><i class="fas fa-calculator"></i> Métricas Clave:</strong>
                        <ul class="mb-0">
                            <li><strong>Deuda Total:</strong> Sumatoria general</li>
                            <li><strong>% Morosidad:</strong> Indicador institucional</li>
                            <li><strong>Representantes Deudores:</strong> Cantidad absoluta</li>
                            <li><strong>Deuda Cambiaria:</strong> Monto en divisas</li>
                            <li><strong>SAF:</strong> Saldo a favor total</li>
                        </ul>
                    </div>
                </div>
            </div>

