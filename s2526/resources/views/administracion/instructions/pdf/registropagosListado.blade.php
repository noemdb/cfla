                            <font size="3" color="#007bff" face="Arial">
                                <strong>¿Por qué algunos botones de anulación están deshabilitados?</strong>
                            </font>
                            <br><br>
                            <font size="2" face="Arial">
                                Los botones de anulación se <strong>deshabilitan automáticamente</strong> cuando el pago
                                no cumple con las condiciones para ser anulado. Esto puede deberse a:
                            </font>
                            <br><br>
                            <ul>
                                <li>El pago ya fue anulado previamente</li>
                                <li>La fecha de anulación ha expirado según la configuración del plan de pago</li>
                                <li>El pago está vinculado a procesos contables posteriores</li>
                                <li>Restricciones administrativas específicas</li>
                            </ul>
