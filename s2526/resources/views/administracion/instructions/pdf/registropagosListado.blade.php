                        <td>
                            <font size="3" color="#155724" face="Arial">
                                <strong>¿Por qué algunos registros tienen fondo azul?</strong>
                            </font>
                            <br><br>
                            <font size="2" face="Arial">
                                El <strong>fondo azul</strong> indica que el pago fue registrado como <strong>Pago
                                    Adelantado</strong>. Esto significa que el pago se aplicó a cuotas que aún no habían
                                vencido al momento del registro. Es un indicador visual para identificar fácilmente
                                estos casos especiales.
                            </font>
                        </td>
                    </tr>
                </table>

