                <!-- Pregunta 2 -->
                <table border="1" cellpadding="10" cellspacing="0" width="100%" bgcolor="#fff3cd">
                    <tr>
                        <td>
                            <font size="3" color="#856404" face="Arial">
                                <strong>¿Cómo interpreto los identificadores P, C y F?</strong>
                            </font>
                            <br><br>
                            <font size="2" face="Arial">
                                <strong>P (Registro Pago):</strong> Identificador único del registro de pago
                                individual<br>
                                <strong>C (Pago Combinado):</strong> Identificador del grupo de pagos combinados<br>
                                <strong>F (Factura/Correlativo):</strong> Número correlativo del comprobante<br><br>
                                Estos identificadores permiten rastrear completamente la transacción through el SAEFL.
                            </font>
                        </td>
                    </tr>
                </table>

                <br>

                <!-- Pregunta 3 -->
                <table border="1" cellpadding="10" cellspacing="0" width="100%" bgcolor="#d4edda">
