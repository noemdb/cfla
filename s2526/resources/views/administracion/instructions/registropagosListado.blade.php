            </h2>

            <div class="accordion" id="faqAccordion">
                <!-- Pregunta 1 -->
                <div class="card">
                    <div class="card-header" id="faq1">
                        <h5 class="mb-0">
                            <button class="btn btn-link" type="button" data-toggle="collapse"
                                data-target="#answer1">
                                <i class="fas fa-question"></i> ¿Por qué algunos botones de anulación están
                                deshabilitados?
                            </button>
                        </h5>
                    </div>
                    <div id="answer1" class="collapse show" data-parent="#faqAccordion">
                        <div class="card-body">
                            Los botones de anulación se <strong>deshabilitan automáticamente</strong> cuando el pago no
                            cumple con las condiciones para ser anulado. Esto puede deberse a:
                            <ul>
                                <li>El pago ya fue anulado previamente</li>
                                <li>La fecha de anulación ha expirado según la configuración del plan de pago</li>
                                <li>El pago está vinculado a procesos contables posteriores</li>
                                <li>Restricciones administrativas específicas</li>
                            </ul>
                        </div>
                    </div>
                </div>

                <!-- Pregunta 2 -->
                <div class="card">
                    <div class="card-header" id="faq2">
                        <h5 class="mb-0">
                            <button class="btn btn-link collapsed" type="button" data-toggle="collapse"
                                data-target="#answer2">
                                <i class="fas fa-question"></i> ¿Cómo interpreto los identificadores P, C y F?
                            </button>
                        </h5>
                    </div>
                    <div id="answer2" class="collapse" data-parent="#faqAccordion">
                        <div class="card-body">
                            <strong>P (Registro Pago):</strong> Identificador único del registro de pago individual<br>
                            <strong>C (Pago Combinado):</strong> Identificador del grupo de pagos combinados<br>
                            <strong>F (Factura/Correlativo):</strong> Número correlativo del comprobante<br><br>
                            Estos identificadores permiten rastrear completamente la transacción through el SAEFL.
                        </div>
                    </div>
                </div>

                <!-- Pregunta 3 -->
                <div class="card">
                    <div class="card-header" id="faq3">
                        <h5 class="mb-0">
                            <button class="btn btn-link collapsed" type="button" data-toggle="collapse"
                                data-target="#answer3">
                                <i class="fas fa-question"></i> ¿Por qué algunos registros tienen fondo azul?
                            </button>
                        </h5>
                    </div>
                    <div id="answer3" class="collapse" data-parent="#faqAccordion">
                        <div class="card-body">
                            El <strong>fondo azul</strong> indica que el pago fue registrado como <strong>Pago
                                Adelantado</strong>. Esto significa que el pago se aplicó a cuotas que aún no habían
                            vencido al momento del registro. Es un indicador visual para identificar fácilmente estos
                            casos especiales.
                        </div>
                    </div>
                </div>

                <!-- Pregunta 4 -->
                <div class="card">
                    <div class="card-header" id="faq4">
                        <h5 class="mb-0">
                            <button class="btn btn-link collapsed" type="button" data-toggle="collapse"
                                data-target="#answer4">
                                <i class="fas fa-question"></i> ¿Qué significa "P.C." en la tabla?
                            </button>
                        </h5>
                    </div>
                    <div id="answer4" class="collapse" data-parent="#faqAccordion">
                        <div class="card-body">
                            <strong>P.C. = Pago Combinado</strong>. Indica si el registro de pago forma parte de un
                            grupo de pagos combinados:
                            <ul>
                                <li><strong>SI:</strong> El pago fue combinado con otros pagos en una misma transacción
                                </li>
                                <li><strong>NO:</strong> El pago fue procesado individualmente</li>
                            </ul>
                            Los pagos combinados permiten aplicar un mismo ingreso a múltiples conceptos/cuotas.
                        </div>
                    </div>
                </div>

                <!-- Pregunta 5 -->
                <div class="card">
                    <div class="card-header" id="faq5">
                        <h5 class="mb-0">
